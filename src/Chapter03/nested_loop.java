package Chapter03;

public class nested_loop {
    public static void main(String[] args) {
        int i = 1; int j = 2; int k = 3;
        if (i > 0){
            if (j > 0){
                int x = 9;
                System.out.println(x);
            }else if (k > 0){
                int y = 8;
                System.out.println(y);
            }
        }else {
            int z = 7;
            System.out.println(z);
        }



    }
}
